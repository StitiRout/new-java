package learnInit;

public class simple36{
public static void main(String[] args) {
		
		int i ,j;
		for ( i=1;i<=5;i++)
		{
			for (j=1;j<=i;j++)
		
			{
		System.out.print(" * ");
	}
		System.out.println();
	}
		int k,l;
		for (k=4;k>=1;k--)
		{
			for (l=k;l>=1;l--)
			{
				System.out.print(" * ");
			}
			System.out.println();
		}
	
    }
    
}
